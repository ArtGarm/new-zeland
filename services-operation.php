                            <div class="text-title"><span>Планирование производства и сбыта:</span></div>
                            <div class="text-body">
                                <p>Во время наших встреч с топ менеджментом компаний, мы часто слышим вопрос: "Какую пользу получит компания от внедрения S&OP (от англ. "Sales & Operations Planning")?" Это вопрос не является необычным, так как и большинство других усилий, направленных на перестройку бизнес процессов организации требует внятного обоснования. Почему же многим компаниям не удается наладить эффективное планирование производства и сбыта?</p>
                            </div>
                            <div class="text-body">
                                <p>Неточное прогнозирование - боязнь невыполнения требований потребителя и, как результат, перепрогнозирование; слишком оптимистичные прогнозы отделов продаж и маркетинга; незапланированные промо-акции; недоверие прогнозам.</p>
                            </div>
                            <div class="text-body">
                                <p>Функциональная оптимизация - фокусирование на стоимости единицы продукции приводящее к увеличению размеров партии и игнорирование влияния этого на стоимость хранения; большие "неэкономичные" минимальные объемы заказа.</p>
                            </div>
                            <div class="text-body">
                                <p>Долгий цикл заказа - процесс ведомый прогнозом, поэтому, требующий инвестиций в запасы до появления спроса; большой портфель заказов; невосприимчивая цепочка поставок ведущая к просрочке или нехватке запасов; увеличение ошибок прогнозирования.Планирование производства и сбыта (S&OP) является эффективной техникой снижающей уровень запасов путем  улучшения точности прогнозирования.</p>
                            </div>
                            <div class="text-title"><span>Наше предложение</span></div>
                            <div class="text-body">
                                <p>Планирование спроса - разработка и внедрение процессов в области сбора, структуризации и коррективы исторических данных; подготовки статистических прогнозов и согласования их в соответствии с планами продаж и маркетинга компании, моделирование возможных сценариев.</p>
                            </div>
                            <div class="text-body">
                                <p>Планирование поставок - оценка возможностей обеспечения потребностей, планирование мощностей, ресурсов, материалов и товарных запасов.</p>
                            </div>
                            <div class="text-body">
                                <p>Улучшение точности прогнозирования путем комплексного анализа, включающего измерение точности статистических прогнозов, оценочные факторы и прогнозные отклонения, внедрение коллективного процесса прогнозирования для выработки консенсусных прогнозов.</p>
