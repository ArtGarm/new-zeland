                                <p>Увеличивающиеся затраты на производство, новые источники конкуренции с низкой себестоимостью ведущие к продолжению давления на рыночные цены, продолжающаяся концентрация рынков и консолидация компаний, подразумевающая что большие и более сильные потребители будут требовать больше от своих поставщиков, в то время как обычные маркетинговые стратегии имеют меньше эффекта в оперативном мире движимом спросом - все это показывает важность и необходимость для компаний, стремящихся быть конкурентоспособными в современном мире, критично взглянуть на свои операционные процессы и оценить эффективность своих цепей поставок.</p>
                            </div>
                            <div class="text-body">
                                <p>Операционный менеджмент включает как разработку, так и управление процессами и ресурсами, необходимыми для производства и доставки товаров и услуг с целью достижения качественного, эффективного сервиса, отвечающего требованиям клиентов организации.</p>
                            </div>
                            <div class="text-title"><span>Наше предложение:</span></div>
                            <div class="text-body">
                                <ul>
                                    <li>
                                        <p>Анализ разрывов (Gap-analysis) между рынком и операционной деятельностью организации, идентификация ключевых проблем и определение путей улучшения результативности.</p>
                                    </li>
                                    <li>
                                        <p>Сегментирование и классифицирование конкурентных факторов производства (OWC - Order winning criteria & OQ - Order qualifiers).</p>
                                    </li>
                                    <li>
                                        <p>Определение целевых параметров результативности (KPI) в рамках ключевых операционных задач, таких как качество, оперативность, надежность, гибкость, инновационность и себестоимость.</p>
                                    </li>
                                    <li>
                                        <p>Разработка и внедрение операционной стратегии.</p>
                                    </li>
                                    <li>
                                        <p>Разработка стандартных операционных процедур (SOP - Standard Operations Procedures) в рамках корпоративной и маркетинговой стратегий организации.</p>
                                    </li>
                                    <li>
                                        <p>Картирование процессов и выбор  оптимальных схем.</p>
                                    </li>
                                    <li>
                                        <p>Управление мощностями и узкими местами в производственных процессах. </p>
                                    </li>
                                    <li>
                                        <p>ТQM (Total Quality Management) - тотальное управление качеством, SPC (Statistical Process Control) - статистическое управление мощностями, FMEA (Failure Mode & Effects Analysis) анализ видов и последствий отказов,  Six Sigma.</p>
                                    </li>
                                    <li>
                                        <p>Внедрение "Lean & JIT (Just-in-time), Kaizen  и других методов повышения эффективности производственных процессов.</p>
