                             <div class="text-title"><span>Управление товарными запасами</span></div>
                            <div class="text-body">
                                <p>Управление товарными запасами играет ключевую роль в эффективной работе практически любой организации. Cокращение неликвидных/ избыточных запасов и инвестиции в создание правильного объема товарных запасов ведут к улучшению уровня обслуживания, более высокой оборачиваемости запасов и более здоровой структуре бизнеса.</p>
                            </div>
                            <div class="text-body">
                                <p>Товарные запасы часто сравниваются с губкой для наличности, которая иначе могла бы быть использована для роста бизнеса. Поэтому, архиважно чтобы организации оптиально планировали уровень запасов т.е. минимизировали инвестиции капитала в запасы, в то же время поддерживая или даже улучшая доступность продуктов.</p>
                            </div>
                            <div class="text-body">
                                <p>Мы поможем "вооружить" вас фундаментальными основами и техникой для анализа и контроля уровня товарных запасов - от техники по снижению себестоимости до ежедневного управления товарными запасами, а также, последними разработками в этой области, такими как "JIT" и "Lean".</p>
                            </div>
                            <div class="text-body">
                                <p>Наши консультанты помогут Вам ответить на такие фундаментальные вопросы как: "Необходим ли заказ в данный момент?" и "Сколько необходимо заказать?". Для этого:</p>
                            </div>
                            <div class="text-title"><span>Наше предложение:</span></div>
                            <div class="text-body">
                                <ul>
                                    <li>
                                        <p>Анализ обоснованности уровня запасов и материалов находящихся в процессе доставки, цикла заказа, прогнозных уровней спроса на период цикла заказа и статистический анализ отклонений в исторических прогнозах.</p>
                                    </li>
                                    <li>
                                        <p>Анализ себестоимости размещения заказа на пополнение товарных запасов и издержек по хранению ТМЗ.</p>
                                    </li>
                                    <li>
                                        <p>Расчет периода подачи заказа (ROP - Re-order point) и уровня резервных запасов (Safety stock).</p>
                                    </li>
                                    <li>
                                        <p>Расчет экономически рентабельного уровня заказа (EOQ- Economic Order Quantity).</p>
                                    </li>
                                    <li>
                                        <p>АBC анализ - сегментирование товарных запасов и т.д.</p>
                                    </li>
                                    <li>
                                        <p>Гравитационный анализ (Gravity analysis) - оптимального расположения складов и распределительных центров.</p>
