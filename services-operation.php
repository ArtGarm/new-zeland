                            <div class="text-title"><span>Интегрированная цепочка поставок</span></div>
                            <div class="text-body">
                                <p>Согласно последним исследованиям AT Kearney, потери в цепях поставок компаний сегодня достигают 25% от общих операционных расходов. Таким образом, несложно посчитать, что при норме прибыли в 10%, даже сокращение потерь в цепочке поставок с вышеупомянутых 25% до 20% может увеличить прибыль компании до 45%!!! Ключевыми областями, наиболее часто показывающими результаты оптимизации, связанной с сокращением расходов, являются управление запасами, транспортировка, хранение и упаковка, улучшение уровня сервиса путем внедрения таких подходов как MTO (Make-to-Order, рус. изготовление на заказ). </p>
                            </div>
                            <div class="text-body">
                                <p>ЭсСиЭм Консалт достаточно гибко подходит к оптимизации операций и цепочек поставок своих клиентов и помимо требований клиентов, в тех или иных вопросах внедрения, в первую очередь, фокусируется на тех направлениях, в которых возможно достижение немедленных результатов (Quick wins) и выгод. Каждая компания, безусловно имеет свою специфику и масштаб, поэтому не существует стандартных решений или подходов. Однако, существуют общепризнанные концепции эффективной и лучшей в мировой практике (Best-in-Class) методологии интегрирования и управления цепями поставок, принципы которой, в той или иной степени, применимы к  любой компании.</p>
                            </div>
                            <div class="text-image"><img src="images/text2.jpg"  alt=""></div>
                            <div class="text-body">
                                <p>В многофазовом процессе интеграции цепочек поставок различные системы Фазы 1, такие как Сорсинг, Производство, Хранение, Распределение (дистрибьюция) и Возвраты не связаны между собой и независимы друг от друга. Фаза 2 интегрирована, в соответствии с единым планом. Краеугольным камнем успешной интеграции на этой стадии являются информационные технологии поддерживающие процесс принятия решений на разных уровнях. Информационные технологии (IT) должны обеспечивать прозрачность потоков связанных с продуктами, услугами и информацией. И третий этап развития управления цепями поставок характеризуется фокусом на глобальных системах взаимоотношений с поставщиками и эскспансией цепей поставок через национальные границы и на другие континенты.</p>
                            </div>
                            <div class="text-body">
                                <p>Как уже было сказано, не существует единого стандарта при внедрении эффективного управления цепями поставок. Однако, типичные стадии внедрения в той или иной степени выполняются в следующей последовательности:</p>
                            </div>
                            <div class="text-title"><span>Наше предложение:</span></div>
                            <div class="text-body">
                                <p>ОЦЕНКА - EVALUATE</p>
                            </div>
                            <div class="text-body">
                                <p>Описание процессов существующей цепочки поставок и идентификация возможностей для оптимизации. Оценка осуществляется в областях взаимодействия с потребителями с момента поступления заказа и до оплаты счета. Товарные транзакции от поставщика до потребителя. Взаимодействие с рынком от совокупного спроса до выполнения заказа.</p>
                            </div>
                            <div class="text-body">
                                <p>ОПРЕДЕЛЕНИЕ - DEFINE</p>
                            </div>
                            <div class="text-body">
                                <p>Разработка видения и стратегии интеграции цепочек поставок, создание оптимальной организационной структуры, внедрение информационных и коммуникационных сетей, трансформация стратегии в действия.  Разработка ключевых индикаторов результативности и измерение эффективности существующей цепочки поставок. Бенчмаркинг результатов. Идентификация слабых мест цепочки поставок.</p>
                            </div>
                            <div class="text-body">
                                <p>ПЛАНИРОВАНИЕ - PLAN</p>
                            </div>
                            <div class="text-body">
                                <p>Подготовка плана действий для устранения неэффективных показателей и дизайн процесса "To-Be" (от англ. "быть" - как должно быть), составление участников проекта, приоритизация, разработка детального плана действий (ресурсы, сроки, результаты подлежащие сдаче).</p>
                            </div>
                            <div class="text-body">
                                <p>ИСПОЛНЕНИЕ - EXECUTE</p>
                            </div>
                            <div class="text-body">
                                <p>Внедрение изменений.</p>
